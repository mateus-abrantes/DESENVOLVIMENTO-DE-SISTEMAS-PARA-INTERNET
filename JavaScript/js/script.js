function soma(x,y){
  var resultado=x+y;
  return resultado;
}

function divisao(x,y){
  var resultado=x/y;
  return resultado;
}

function multiplicacao(x,y){
  var resultado=x*y;
  return resultado;
}

function subtracao(x,y){
  var resultado=x-y;
  return resultado;
}

function get_value(a){
  var x = parseInt(document.getElementById(a).value);
  return x;
}
function get_value_s(a){
  var x = document.getElementById(a).value;
  return x;
}
